package com.iqianbang.fanpai.bean;

public class HuiKuanDetailBean {
	private String planRepayTime;
	private String amount;
	private String status;
	private String statusDesc;

	public String getPlanRepayTime() {
		return planRepayTime;
	}

	public void setPlanRepayTime(String planRepayTime) {
		this.planRepayTime = planRepayTime;
	}

	public String getAmount() {
		return amount;
	}

	public void setAmount(String amount) {
		this.amount = amount;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getStatusDesc() {
		return statusDesc;
	}

	public void setStatusDesc(String statusDesc) {
		this.statusDesc = statusDesc;
	}
}
